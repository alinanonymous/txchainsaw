public class Item {
  public String name;
  public String desc;
  public String use;

  public void setDesc(String description) {
    desc = description;
  }

  public void setUse(String useString) {
    use = useString;
  }

  public String getDesc() {
    return desc;
  }

  public String getUse() {
    return use;
  }

}